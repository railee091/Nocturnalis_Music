 function load_product()
 {
  $.ajax({
   url:"fetch_item.php",
   method:"POST",
   success:function(data)
   {
    $('#display_item').html(data);
   }
  })
 }

 function load_cart_data()
 {
  $.ajax({
   url:"fetch_cart.php",
   method:"POST",
   dataType:"json",
   success:function(data)
   {
    $('#cart_details').html(data.cart_details);
    $('.total_price').text(data.total_price);
    $('.badge').text(data.total_item);
    if(data.total_price == '$0.00'){
    $('#check_out_cart').addClass('disabled');
    	$("#check_out_cart").text('empty');
	}else{
	$('#check_out_cart').removeClass('disabled');	
		$("#check_out_cart").text('checkout');
	}
   }
  })
 }

 $('#cart-popover').popover({
  html : true,
  container : 'body',
  content:function(){
   return $('#popover_content_wrapper').html();
  }
 });

 $(document).on('click', '.add_to_cart', function(){
  var product_id = $(this).attr('id');
  var product_name = $('#name'+product_id+'').val();
  var product_price = $('#price'+product_id+'').val();
  var product_quantity = $('#quantity'+product_id).val();
  var action = 'add';
  if(product_quantity > 0)
  {
   $.ajax({
    url:"action.php",
    method:"POST",
    data:{product_id:product_id, product_name:product_name, product_price:product_price, product_quantity:product_quantity, action:action},
    success:function(data)
    {
     load_cart_data();
     /**alert("Item has been Added into Cart");**/
    }
   })
  }
  else
  {
   alert("Please Enter Number of Quantity");
  }
  if(product_quantity == 1 && product_quantity < 2 ){
  	$('#'+product_id).attr('disabled','disabled');
  	$('#'+product_id).addClass('btn-danger');
  	$('#'+product_id).addClass('removeButton-'+product_id);
  	$('#'+product_id).val('added to cart');
  }
 });

 $(document).on('click', '.delete', function(){
  var product_id = $(this).attr('id');
  var action = 'remove';
  if(confirm("Are you sure you want to remove this product?"))
  {
   $.ajax({
    url:"action.php",
    method:"POST",
    data:{product_id:product_id, action:action},
    success:function(data)
    {
     load_cart_data();
     $('#cart-popover').popover('hide');
     //alert("Item has been removed from Cart");
    }
   })
  }
  else
  {
   return false;
  }
 });

$(document).on('click', '.delete', function(){
  var product_id = $(this).attr('id');
    $('.removeButton-'+product_id).attr('disabled',false);
    $('.removeButton-'+product_id).removeClass('btn-danger');
    $('.removeButton-'+product_id).addClass('btn-success');
    $('.removeButton-'+product_id).val('add to cart');

 });

